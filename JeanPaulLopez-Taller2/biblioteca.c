#include <math.h>

double suma(double a, double b) {
   return a + b;
}

double resta(double a, double b) {
   return a - b;
}

double division(double a, double b) {
   return a / b;
}


double multiplicacion(double a, double b) {
   return a * b;
}


double raizCuadrada(double a) {
   return sqrt(a);
}


